int		*ft_range(int min, int max)
{
	int *new;
	int i;
	int j;

	i = max - min;
	if (i < 0)
		return ;
	j = 0;
	new = (int *)malloc(i);
	while (j < i)
	{
		new[j] = min;
		min++;
		j++;
	}
	return (new);
}
