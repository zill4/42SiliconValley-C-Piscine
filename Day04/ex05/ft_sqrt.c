int		ft_sqrt(int nb)
{
	int root;
	int ans;

	root = 1;
	if (nb == 0)
	{
		return (0);
	}
	while ((root * root) < nb)
	{
		root++;
	}
	if ((nb % root) == 0)
		return (root);
	else
		return (0);
}
