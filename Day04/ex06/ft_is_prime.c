int		ft_is_prime(int nb)
{
	int nb2;

	nb2 = nb;
	if (nb2 <= 0)
	{
		return (0);
	}
	while (nb2 > 1)
	{
		nb2--;
		if (nb % nb2 == 0 && nb2 > 1)
		{
			return (0);
		}
	}
	return (1);
}
