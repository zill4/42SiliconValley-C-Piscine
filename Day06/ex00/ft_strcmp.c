int 	ft_strcmp(char *s1, char *s2)
{
		int i;
		
		i = 0;
		if (s2[0] == '\0' && s1[0] == '\0')
			return (0);
		if (s2[0]  == '\0')
			return (s1[0]);
		if (s1[0] == '\0')
			return (s2[0]);
		while (s1[i] != '\0' )
		{
			if (s2[i] != s1[i])
				return (s1[i] - s2[i]);
			i++;
		}
		return (0);
}
